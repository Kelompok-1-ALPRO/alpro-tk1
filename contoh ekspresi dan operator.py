# Contoh ekspresi dan operator aritmatika

# variabel
a = 20
b = 7

# penjumlahan
penjumlahan = a + b
print(f"Hasil dari Penjumlahan {a} + {b} adalah: {penjumlahan}")

# pengurangan 
pengurangan = a - b
print(f"Hasil dari Pengurangan {a} - {b} adalah: {pengurangan}")

# Perkalian
perkalian = a * b
print(f"Hasil dari Perkalian {a} * {b} adalah: {perkalian}")

# pembagian
pembagian = a / b
print(f"Hasil dari Pembagian {a} / {b} adalah: {pembagian}")

# eksponen
eksponen = a ** b 
print(f"Hasil dari Eksponen {a} ** {b} adalah: {eksponen}")

# modulus
modulus = a % b
print(f"Hasil dari Modulus {a} % {b} adalah: {modulus}")

# pembagian bulat
pembagian_bulat = a // b
print(f"Hasil dari Pembagian bulat {a} // {b} = {pembagian_bulat}")
