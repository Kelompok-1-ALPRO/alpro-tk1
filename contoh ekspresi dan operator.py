# Contoh ekspresi dan operator aritmatika

# variabel
a = 76
b = 98

# penjumlahan
penjumlahan = a + b
print(f"Hasil dari Penjumlahan {a} + {b} adalah: {penjumlahan}")

# pengurangan 
pengurangan = a - b
print(f"Hasil dari Pengurangan {a} + {b} adalah: {pengurangan}")

# Perkalian
perkalian = a * b
print(f"Hasil dari Perkalian {a} + {b} adalah: {perkalian}")
